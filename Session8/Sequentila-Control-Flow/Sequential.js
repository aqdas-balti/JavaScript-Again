/*
1. Sequential (Default Mode)
Introduction:
Sequential execution means the code runs line by line from top to bottom unless a special statement (like a condition or loop) changes the flow.

Example:*/
console.log("Step 1"); // This will execute first
console.log("Step 2"); // Then this will execute second
console.log("Step 3"); // Finally, this will execute third
/*
Explanation:
console.log("Step 1");
Pehla line chal kar screen par "Step 1" print karega.

console.log("Step 2");
Iske baad dusra line chal kar "Step 2" print karega.

console.log("Step 3");
Akhri line chal kar "Step 3" print karega.
*/